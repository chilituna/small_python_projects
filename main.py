alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def caesar(original_text, shift_amount, coding):
    encrypted_text = ''
    if coding == 'decode':
        shift_amount *= -1
    for letter in original_text:
        if letter in alphabet:
            new_position = alphabet.index(letter) + shift_amount
            new_position %= len(alphabet)
            encrypted_text += alphabet[new_position]
        else:
            encrypted_text += letter

    print(f"Here is the {coding}d result: {encrypted_text}\n")

def run_program():
    looping = True
    while looping:
        direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n").lower()
        while direction != 'encode' and direction != 'decode':
            print("Incorrect input. Please try again.\n")
            direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n").lower()
        text = input("Type your message:\n").lower()
        shift = int(input("Type the shift number:\n"))
        caesar(text, shift, direction)
        if input("Type 'yes' to continue. Type anything else to quit:\n").lower() != 'yes':
            looping = False

run_program()








